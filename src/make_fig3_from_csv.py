#!/usr/bin/env python3
"""
Create Figure 3 (group means + 95% CI) from fig3_group_table.csv.

Input CSV schema (columns expected):
group,n_runs,MAE_mean,MAE_ci_lo,MAE_ci_hi,RMSE_mean,RMSE_ci_lo,RMSE_ci_hi

Outputs:
- fig3_severity_ladder.pdf (and .png)
- fig3_pgfplots_data.tex (PGFPlots coordinates + errors)
- fig3_group_table_clean.csv (optional cleaned/rounded)
"""

from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt


ORDER = [
    "source-test (Exp10–18)",
    "target (Exp1–9)",
    "alt (Exp19–27)",
]

RENAME = {
    # handle common encoding artifact
    "source-test (Exp10â€“18)": "source-test (Exp10–18)",
    "target (Exp1â€“9)": "target (Exp1–9)",
    "alt (Exp19â€“27)": "alt (Exp19–27)",
    # allow short labels too
    "Source-test": "source-test (Exp10–18)",
    "Target": "target (Exp1–9)",
    "Alt": "alt (Exp19–27)",
}


def load_and_clean(csv_path: Path) -> pd.DataFrame:
    df = pd.read_csv(csv_path)
    if "group" not in df.columns:
        raise ValueError("CSV must contain a 'group' column.")

    df["group"] = df["group"].astype(str).replace(RENAME)
    # fix remaining “â€“” if any
    df["group"] = df["group"].str.replace("â€“", "–", regex=False)

    # ensure numeric
    num_cols = [
        "n_runs",
        "MAE_mean", "MAE_ci_lo", "MAE_ci_hi",
        "RMSE_mean", "RMSE_ci_lo", "RMSE_ci_hi",
    ]
    for c in num_cols:
        if c in df.columns:
            df[c] = pd.to_numeric(df[c], errors="coerce")

    # reorder
    df = df.set_index("group").reindex([g for g in ORDER if g in df["group"].values or g in df.index])
    df = df.reset_index()

    # sanity check
    missing = [g for g in ORDER if g not in df["group"].tolist()]
    if missing:
        print("[WARN] Missing groups in input CSV:", missing)

    return df


def save_clean_csv(df: pd.DataFrame, out_csv: Path, decimals: int = 2) -> None:
    df2 = df.copy()
    for c in ["MAE_mean", "MAE_ci_lo", "MAE_ci_hi", "RMSE_mean", "RMSE_ci_lo", "RMSE_ci_hi"]:
        if c in df2.columns:
            df2[c] = df2[c].round(decimals)
    df2.to_csv(out_csv, index=False)


def write_pgfplots_tex(df: pd.DataFrame, out_tex: Path) -> None:
    """
    Writes two PGFPlots tables as LaTeX macros:
    \pgfplotstableread{...}\FigThreeMAE
    \pgfplotstableread{...}\FigThreeRMSE
    """
    lines_mae = ["x y ylo yhi"]
    lines_rmse = ["x y ylo yhi"]

    for i, row in df.iterrows():
        x = i + 1
        mae = float(row["MAE_mean"])
        mae_lo = float(row["MAE_ci_lo"])
        mae_hi = float(row["MAE_ci_hi"])
        rmse = float(row["RMSE_mean"])
        rmse_lo = float(row["RMSE_ci_lo"])
        rmse_hi = float(row["RMSE_ci_hi"])

        # PGFPlots wants + and - errors; we store absolute bounds then compute in LaTeX,
        # or store lower/upper directly and use y error plus/minus with expressions.
        lines_mae.append(f"{x} {mae:.6f} {mae_lo:.6f} {mae_hi:.6f}")
        lines_rmse.append(f"{x} {rmse:.6f} {rmse_lo:.6f} {rmse_hi:.6f}")

    tex = []
    tex.append("% Auto-generated by make_fig3_from_csv.py")
    tex.append("% Columns: x y ylo yhi  (ylo/yhi are CI bounds)")
    tex.append("\\pgfplotstableread{")
    tex.extend(lines_mae)
    tex.append("}{\\FigThreeMAE}")
    tex.append("")
    tex.append("\\pgfplotstableread{")
    tex.extend(lines_rmse)
    tex.append("}{\\FigThreeRMSE}")
    tex.append("")

    out_tex.write_text("\n".join(tex), encoding="utf-8")


def plot_matplotlib(df: pd.DataFrame, out_pdf: Path, out_png: Path) -> None:
    groups = df["group"].tolist()
    x = np.arange(len(groups)) + 1

    mae = df["MAE_mean"].to_numpy()
    mae_lo = df["MAE_ci_lo"].to_numpy()
    mae_hi = df["MAE_ci_hi"].to_numpy()
    mae_err = np.vstack([mae - mae_lo, mae_hi - mae])

    rmse = df["RMSE_mean"].to_numpy()
    rmse_lo = df["RMSE_ci_lo"].to_numpy()
    rmse_hi = df["RMSE_ci_hi"].to_numpy()
    rmse_err = np.vstack([rmse - rmse_lo, rmse_hi - rmse])

    plt.style.use("seaborn-v0_8-whitegrid")
    fig, axes = plt.subplots(1, 2, figsize=(9.2, 3.6), constrained_layout=True)

    axes[0].bar(x, mae, color="#4C72B0", width=0.6)
    axes[0].errorbar(x, mae, yerr=mae_err, fmt="none", ecolor="black", capsize=4, lw=1)
    axes[0].set_xticks(x, groups, rotation=15, ha="right")
    axes[0].set_ylabel("MAE (s)")
    axes[0].set_title("Fig3a — MAE (mean ± 95% CI)")

    axes[1].bar(x, rmse, color="#55A868", width=0.6)
    axes[1].errorbar(x, rmse, yerr=rmse_err, fmt="none", ecolor="black", capsize=4, lw=1)
    axes[1].set_xticks(x, groups, rotation=15, ha="right")
    axes[1].set_ylabel("RMSE (s)")
    axes[1].set_title("Fig3b — RMSE (mean ± 95% CI)")

    fig.savefig(out_pdf, bbox_inches="tight")
    fig.savefig(out_png, dpi=250, bbox_inches="tight")
    plt.close(fig)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--in_csv", required=True, help="Path to fig3_group_table.csv (group means + CI).")
    ap.add_argument("--out_dir", default="outputs/figures", help="Where to write figure + PGFPlots files.")
    ap.add_argument("--stem", default="fig3_severity_ladder", help="Base filename stem.")
    ap.add_argument("--write_clean_csv", action="store_true", help="Also write cleaned/rounded CSV.")
    args = ap.parse_args()

    in_csv = Path(args.in_csv)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    df = load_and_clean(in_csv)

    out_pdf = out_dir / f"{args.stem}.pdf"
    out_png = out_dir / f"{args.stem}.png"
    out_tex = out_dir / f"{args.stem}_pgfplots_data.tex"
    out_clean = out_dir / f"{args.stem}_clean.csv"

    plot_matplotlib(df, out_pdf, out_png)
    write_pgfplots_tex(df, out_tex)
    if args.write_clean_csv:
        save_clean_csv(df, out_clean, decimals=2)

    print("[OK] Wrote:")
    print(" -", out_pdf)
    print(" -", out_png)
    print(" -", out_tex)
    if args.write_clean_csv:
        print(" -", out_clean)


if __name__ == "__main__":
    main()
