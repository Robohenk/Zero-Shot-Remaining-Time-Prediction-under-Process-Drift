#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
Convert Fig5 output CSV into PGFPlots-friendly inputs.

Input CSV (either):
- outputs/fig5/fig5_scatter_points.csv   (from run_figures_2to5.py fig5)
- outputs/figures/fig5_points.csv        (from plot_fig5_drift_vs_error.py)

Expected columns (case-insensitive):
- drift_score  (or DriftScore etc.)
- MAE          (or mae)
- group        (scenario group label)

Outputs (to --out_dir):
- fig5_points_source_test.csv  (drift_score, mae)
- fig5_points_target.csv       (drift_score, mae)
- fig5_points_alt.csv          (drift_score, mae)
- fig5_points_all.csv          (drift_score, mae, group)
- fig5_tables.tex              (pgfplotstableread convenience)
- fig5_stats.tex               (Spearman rho macro)

Usage:
  python make_pgfplots_fig5_input.py --in_csv outputs/fig5/fig5_scatter_points.csv --out_dir figures/fig5_points
"""

from __future__ import annotations
import argparse
import re
from pathlib import Path
import pandas as pd


def read_csv_flex(path: Path) -> pd.DataFrame:
    for enc in ("utf-8", "utf-8-sig", "cp1252", "latin1"):
        try:
            return pd.read_csv(path, encoding=enc)
        except Exception:
            pass
    return pd.read_csv(path, encoding="latin1")


def norm_text(s: str) -> str:
    s = str(s)
    # fix mojibake dash from some Windows CSVs
    s = s.replace("â€“", "–").replace("â€”", "—")
    s = re.sub(r"\s+", " ", s).strip()
    return s


def find_col(df: pd.DataFrame, candidates: list[str]) -> str:
    cols = {c.lower(): c for c in df.columns}
    for cand in candidates:
        if cand.lower() in cols:
            return cols[cand.lower()]
    raise KeyError(f"Missing one of columns {candidates}. Available: {list(df.columns)}")


def canonical_group(g: str) -> str:
    g0 = norm_text(g)
    gl = g0.lower()
    if "source" in gl and "test" in gl:
        return "source-test (Exp10–18)"
    if "target" in gl:
        return "target (Exp1–9)"
    if "alt" in gl:
        return "alt (Exp19–27)"
    # if your file contains only evaluation points, this fallback is still fine
    return g0


def slug(g: str) -> str:
    gl = g.lower()
    if "source-test" in gl:
        return "source_test"
    if "target" in gl:
        return "target"
    if "alt" in gl:
        return "alt"
    gl = re.sub(r"[^\w]+", "_", gl).strip("_")
    return gl[:60] if gl else "group"


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--in_csv", required=True, help="Fig5 scatter points CSV")
    ap.add_argument("--out_dir", required=True, help="Output directory for PGFPlots CSVs")
    args = ap.parse_args()

    in_csv = Path(args.in_csv)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    df = read_csv_flex(in_csv)

    c_group = find_col(df, ["group"])
    c_x = find_col(df, ["drift_score", "driftscore", "drift"])
    c_y = find_col(df, ["mae", "MAE"])

    df = df.rename(columns={c_group: "group", c_x: "drift_score", c_y: "mae"}).copy()
    df["group"] = df["group"].map(canonical_group)

    df["drift_score"] = pd.to_numeric(df["drift_score"], errors="coerce")
    df["mae"] = pd.to_numeric(df["mae"], errors="coerce")
    df = df.dropna(subset=["drift_score", "mae"])

    # Export "all points" (small: one point per run)
    df[["drift_score", "mae", "group"]].to_csv(
        out_dir / "fig5_points_all.csv", index=False, encoding="utf-8", float_format="%.8g"
    )

    # Per-group exports
    groups = [
        "source-test (Exp10–18)",
        "target (Exp1–9)",
        "alt (Exp19–27)",
    ]

    written = []
    for g in groups:
        sub = df[df["group"] == g][["drift_score", "mae"]].sort_values("drift_score")
        if sub.empty:
            continue
        fname = f"fig5_points_{slug(g)}.csv"
        sub.to_csv(out_dir / fname, index=False, encoding="utf-8", float_format="%.8g")
        written.append((g, fname))

    # Spearman rho (over all points in df)
    rho = float(df[["drift_score", "mae"]].corr(method="spearman").iloc[0, 1])
    stats_tex = out_dir / "fig5_stats.tex"
    stats_tex.write_text(
        "% Auto-generated by make_pgfplots_fig5_input.py\n"
        f"\\newcommand{{\\FigFiveSpearman}}{{{rho:.2f}}}\n",
        encoding="utf-8"
    )

    # Convenience tables file
    tables_tex = out_dir / "fig5_tables.tex"
    lines = ["% Auto-generated by make_pgfplots_fig5_input.py\n"]
    for g, fname in written:
        cmd = {
            "source-test (Exp10–18)": "\\FigFiveSource",
            "target (Exp1–9)": "\\FigFiveTarget",
            "alt (Exp19–27)": "\\FigFiveAlt",
        }.get(g, f"\\FigFive{slug(g).title().replace('_','')}")
        lines.append(f"\\pgfplotstableread{{{(out_dir / fname).as_posix()}}}{cmd}\n")
    lines.append(f"\\input{{{stats_tex.as_posix()}}}\n")
    tables_tex.write_text("".join(lines), encoding="utf-8")

    print("[OK] Wrote PGFPlots inputs to:", out_dir.resolve())
    for g, fname in written:
        print(" -", fname, "(", g, ")")
    print(" - fig5_points_all.csv")
    print(" - fig5_tables.tex")
    print(" - fig5_stats.tex  (\\FigFiveSpearman)")


if __name__ == "__main__":
    main()
